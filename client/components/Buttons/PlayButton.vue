<template>
  <div class="d-inline-flex">
    <v-btn
      v-if="canPlay(item) && fab"
      fab
      color="primary"
      :loading="loading"
      @click.prevent="playOrResume"
    >
      <v-icon size="36">mdi-play</v-icon>
    </v-btn>
    <v-btn
      v-else-if="!fab"
      :disabled="!canPlay(item)"
      :loading="loading"
      class="mr-2"
      color="primary"
      min-width="8em"
      depressed
      rounded
      @click="playOrResume"
    >
      {{
        shuffle
          ? $t('playback.shuffle')
          : canResume(item)
          ? $t('resume')
          : $t('play')
      }}
    </v-btn>
  </div>
</template>

<script lang="ts">
import { BaseItemDto } from '@jellyfin/client-axios';
import Vue from 'vue';
import { mapActions, mapState } from 'vuex';
import itemHelper from '~/mixins/itemHelper';
import timeUtils from '~/mixins/timeUtils';
import { PlaybackStatus } from '~/store/playbackManager';

export default Vue.extend({
  mixins: [itemHelper, timeUtils],
  props: {
    item: {
      type: Object as () => BaseItemDto,
      required: true
    },
    fab: {
      type: Boolean
    },
    shuffle: {
      type: Boolean
    }
  },
  data() {
    return {
      loading: false
    };
  },
  computed: {
    ...mapState('playbackManager', ['status'])
  },
  watch: {
    status(): void {
      if (this.status === PlaybackStatus.playing) {
        this.loading = false;
      }
    }
  },
  methods: {
    ...mapActions('playbackManager', ['play']),
    playOrResume(): void {
      this.loading = true;

      if (this.item && this.canResume(this.item)) {
        this.play({
          item: this.item,
          startFromTime:
            this.ticksToMs(this.item.UserData?.PlaybackPositionTicks) / 1000
        });
      } else if (this.shuffle) {
        // We force playback from the start when shuffling, since you wouldn't resume AND shuffle at the same time
        this.play({
          item: this.item,
          startShuffled: true
        });
      } else {
        this.play({
          item: this.item
        });
      }
    }
  }
});
</script>
